namespace Calculator
{
    public class Math
    {
        public float Add(float num1, float num2)
        {
            return num1 + num2;
        }

        public float Sub(float num1, float num2)
        {
            return num1 - num2;
        }

        public float Multi(float num1, float num2)
        {
            return num1 * num2;
        }

        public float Div(float num1, float num2)
        {
            return num1 / num2;
        }
    }
}
